    private fun isSoftBankEmoji(c: Char): Boolean {
        return (c.code shr 12) == 0xe
    }

    private fun getEmojiResource(codePoint: Int): Int {
        return emojisMap[codePoint]
    }

    private fun getSoftbankEmojiResource(c: Char): Int {
        return softbanksMap[c.code]
    }

    /**
     * Convert emoji characters of the given Spannable to the according emojicon.
     *
     * @param context
     * @param text
     * @param emojiSize
     */
    fun addEmojis(context: Context, text: Spannable, emojiSize: Int) {
        addEmojis(context, text, emojiSize, 0, -1)
    }

    /**
     * Convert emoji characters of the given Spannable to the according emojicon.
     *
     * @param context
     * @param text
     * @param emojiSize
     * @param index
     * @param length
     */
    fun addEmojis(context: Context, text: Spannable, emojiSize: Int, index: Int, length: Int) {
        val textLength = text.length
        val textLengthToProcessMax = textLength - index
        val textLengthToProcess = if (length < 0 || length >= textLengthToProcessMax) textLength else (length + index)

        // remove spans throughout all text
        val oldSpans = text.getSpans(0, textLength, EmojiconSpan::class.java)
        for (oldSpan in oldSpans) {
            text.removeSpan(oldSpan)
        }

        var skip: Int
        var i = index
        while (i < textLengthToProcess) {
            skip = 0
            var icon = 0
            val c = text[i]
            if (isSoftBankEmoji(c)) {
        icon = getSoftbankEmojiResource(c)
        skip = if (icon == 0) 0 else 1
            }

            if (icon == 0) {
        val unicode = Character.codePointAt(text, i)
        skip = Character.charCount(unicode)

        if (unicode > 0xff) {
            icon = getEmojiResource(unicode)
        }

        if (icon == 0 && i + skip < textLengthToProcess) {
            val followUnicode = Character.codePointAt(text, i + skip)
            if (followUnicode == 0x20e3) {
                var followSkip = Character.charCount(followUnicode)
                when (unicode) {
                    0x0031 -> icon = R.drawable.emoji_0031
                    0x0032 -> icon = R.drawable.emoji_0032
                    0x0033 -> icon = R.drawable.emoji_0033
                    0x0034 -> icon = R.drawable.emoji_0034
                    0x0035 -> icon = R.drawable.emoji_0035
                    0x0036 -> icon = R.drawable.emoji_0036
                    0x0037 -> icon = R.drawable.emoji_0037
                    0x0038 -> icon = R.drawable.emoji_0038
                    0x0039 -> icon = R.drawable.emoji_0039
                    0x0030 -> icon = R.drawable.emoji_0030
                    0x0023 -> icon = R.drawable.emoji_0023
                    else -> followSkip = 0
                }
                skip += followSkip
            } else {
                var followSkip = Character.charCount(followUnicode)
                when (unicode) {
                    0x1f1ef -> icon = if (followUnicode == 0x1f1f5) R.drawable.emoji_1f1ef_1f1f5 else 0
                    0x1f1fa -> icon = if (followUnicode == 0x1f1f8) R.drawable.emoji_1f1fa_1f1f8 else 0
                    0x1f1eb -> icon = if (followUnicode == 0x1f1f7) R.drawable.emoji_1f1eb_1f1f7 else 0
                    0x1f1e9 -> icon = if (followUnicode == 0x1f1ea) R.drawable.emoji_1f1e9_1f1ea else 0
                    0x1f1ee -> icon = if (followUnicode == 0x1f1f9) R.drawable.emoji_1f1ee_1f1f9 else 0
                    0x1f1ec -> icon = if (followUnicode == 0x1f1e7) R.drawable.emoji_1f1ec_1f1e7 else 0
                    0x1f1ea -> icon = if (followUnicode == 0x1f1f8) R.drawable.emoji_1f1ea_1f1f8 else 0
                    0x1f1f7 -> icon = if (followUnicode == 0x1f1fa) R.drawable.emoji_1f1f7_1f1fa else 0
                    0x1f1e8 -> icon = if (followUnicode == 0x1f1f3) R.drawable.emoji_1f1e8_1f1f3 else 0
                    0x1f1f0 -> icon = if (followUnicode == 0x1f1f7) R.drawable.emoji_1f1f0_1f1f7 else 0
                    else -> followSkip = 0
                }
                skip += followSkip
            }
        }
            }

            if (icon > 0) {
                text.setSpan(EmojiconSpan(context, icon, emojiSize), i, i + skip, Spannable.SPAN_EXCLUSIVE_EXCLUSIVE)
            }
            i += skip
        }
    }
